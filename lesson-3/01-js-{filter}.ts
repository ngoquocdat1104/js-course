
//Bài 1: Tạo một mảng scores chứa các giá trị điểm của học sinh: 85, 90, 78. filter >= 80
let scores = [85, 90, 78]
let biggerThan80 = scores.filter(number => number >= 70)
console.log(biggerThan80)

// Bài 2: Tạo một mảng ages chứa các giá trị tuổi: 18, 21, 16, 25. filter >= 18

let ages = [18, 21, 16, 25]
let biggerThan18 = ages.filter(age => age >= 18)
console.log(biggerThan18)


